    private void initServer() throws HeadlessException {
        try {
            s = new Server(Server.DEFAULT_PORT);
        } catch (IOException ex) {
//            Functions.showMessageDialog("ERROR!!", "Unable to start server !!! error : \n" + ex.getMessage() + "\ncause : " + ex.getCause());
        }
        t = new Timer(1000, new ActionListener() {

            @Override
            public synchronized void actionPerformed(ActionEvent e) {
                doGet();
                t.stop();
            }
        });
    }

