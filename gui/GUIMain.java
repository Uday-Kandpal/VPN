    public synchronized void doGet() {

        java.awt.EventQueue.invokeLater(new Runnable() {
            int index = 0;

            @Override
            public void run() {
                while (true) {
                    try {
                        requestFocus();
                        s.connect();
                        System.out.println("started server");
                        String data = "";
                        char c;
                        while (true) {
                            data = s.receive();
                            index++;
                            System.out.print("received : " + data);
                            JOptionPane.showMessageDialog(rootPane, s.getConnectedAddress() + " : " + data.replace("Notification:", ""));
                            break;
                        }
                        s.disconnect();
                    } catch (IOException ex) {
                        Logger.getLogger(Profile.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });

    }

